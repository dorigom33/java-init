package p02;

public class ForLoop {

	public static void main(String[] args) {
		for(int i = 1; i<=9; i++) {
			System.out.println(1 + " X " + i + " = " + i);
		}
		for(int i = 1; i<=9; i++) {
			System.out.println(i + " X " + 1 + " = " + i);
		}
		for(int i = 1; i<=9; i++) {
			System.out.println((10-i) + " X " + 1 + " = " + (10-i));
		}

	}

}
